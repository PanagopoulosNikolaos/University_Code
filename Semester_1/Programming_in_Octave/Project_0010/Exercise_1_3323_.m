function main() % Κύρια συνάρτηση: Αρχικοποιεί την κύρια συνάρτηση
    % Ορισμός πίνακα A
    A = [
        1  0 -1  0;
        0  2  3 -1;
       -1  3  2  2;
        0 -1  2  1
    ];

    % Ορισμός διανυσμάτων b1 και b2
    b1 = [1; 0; -1; 1];
    b2 = [0; -1; 0; 1];

    % Ενσωματωμένη συνάρτηση lu(): Εκτελεί αποσύνθεση LU του πίνακα A
    % Επιστρέφει L (κάτω τριγωνικός), U (άνω τριγωνικός), και P (πίνακας μετάθεσης)
    [L, U, P] = lu(A); 

    % Επίλυση για το b1
    % Ενσωματωμένος τελεστής *: Πολλαπλασιασμός πινάκων P*b1
    y1 = SolForward(L, P*b1);
    x1 = SolBackward(U, y1);

    % Επίλυση για το b2
    % Ενσωματωμένος τελεστής *: Πολλαπλασιασμός πινάκων P*b2
    y2 = SolForward(L, P*b2);
    x2 = SolBackward(U, y2);

    % Ενσωματωμένη συνάρτηση disp(): Εμφανίζει κείμενο ή τιμές πίνακα
    disp('Λύση για το b1:');
    disp(x1);

    disp('Λύση για το b2:');
    disp(x2);
end

main();

function y = SolForward(L, b)
    % Ενσωματωμένη συνάρτηση length(): Επιστρέφει το μήκος του διανύσματος b
    n = length(b);
    % Ενσωματωμένη συνάρτηση zeros(): Δημιουργεί πίνακα μηδενικών με καθορισμένες διαστάσεις
    y = zeros(n,1);
    
    % Βρόχος for: Επαναλαμβάνει από 1 έως n για προς τα εμπρός επίλυση
    for i = 1:n
        % Ανάθεση: Αρχικοποίηση του y(i) με την τιμή του b(i)
        y(i) = b(i);
        % Εσωτερικός βρόχος for: Υπολογίζει το άθροισμα των προηγούμενων όρων
        for j = 1:i-1
            % Ανάθεση: Ενημερώνει το y(i) αφαιρώντας το γινόμενο L(i,j)*y(j)
            y(i) = y(i) - L(i,j)*y(j);
        end
        % Ανάθεση: Διαιρεί το y(i) με το διαγώνιο στοιχείο L(i,i)
        y(i) = y(i) / L(i,i);
    end
end

function x = SolBackward(U, y)
    % Ενσωματωμένη συνάρτηση length(): Επιστρέφει το μήκος του διανύσματος y
    n = length(y);
    % Ενσωματωμένη συνάρτηση zeros(): Δημιουργεί πίνακα μηδενικών με καθορισμένες διαστάσεις
    x = zeros(n,1);
    % Βρόχος for: Επαναλαμβάνει από n έως 1 για προς τα πίσω επίλυση
    for i = n:-1:1
        % Ανάθεση: Αρχικοποίηση του x(i) με την τιμή του y(i)
        x(i) = y(i);
        % Εσωτερικός βρόχος for: Υπολογίζει το άθροισμα των επόμενων όρων
        for j = i+1:n
            % Ανάθεση: Ενημερώνει το x(i) αφαιρώντας το γινόμενο U(i,j)*x(j)
            x(i) = x(i) - U(i,j)*x(j);
        end
        % Ανάθεση: Διαιρεί το x(i) με το διαγώνιο στοιχείο U(i,i)
        x(i) = x(i) / U(i,i);
    end
end
