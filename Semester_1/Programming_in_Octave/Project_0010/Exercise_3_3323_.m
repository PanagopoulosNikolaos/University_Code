function main()
    % Άσκηση 3: Ανάλυση Ιδιοτιμών και Ιδιοδιανυσμάτων
    
    % Δοκιμή της συνάρτησης με τον πίνακα B
    B = [1 5 10; 5 1 3; 10 3 1];
    disp('Ανάλυση για τον πίνακα B:'); % Εμφανίζει μήνυμα στην οθόνη
    disp('Πίνακας B ='); % Εμφανίζει μήνυμα στην οθόνη
    disp(B); % Εμφανίζει τον πίνακα B στην οθόνη
    [eigenvals_B, char_poly_B] = calculate_eigenvalues(B); % Καλεί τη συνάρτηση για υπολογισμό ιδιοτιμών και χαρακτηριστικού πολυωνύμου
    disp('Συντελεστές χαρακτηριστικού πολυωνύμου:'); % Εμφανίζει μήνυμα στην οθόνη
    disp(char_poly_B); % Εμφανίζει τους συντελεστές του χαρακτηριστικού πολυωνύμου
    disp('Ιδιοτιμές του B:'); % Εμφανίζει μήνυμα στην οθόνη
    disp(eigenvals_B); % Εμφανίζει τις ιδιοτιμές του πίνακα B

    % Ανάλυση για τον πίνακα A2
    A2 = [-1 5 2; 5 -1 2; 2 2 2];
    disp('Ανάλυση για τον πίνακα A2:'); % Εμφανίζει μήνυμα στην οθόνη
    disp('Πίνακας A2 ='); % Εμφανίζει μήνυμα στην οθόνη
    disp(A2); % Εμφανίζει τον πίνακα A2 στην οθόνη
    [eigenvals_A2, char_poly_A2] = calculate_eigenvalues(A2); % Καλεί τη συνάρτηση για υπολογισμό ιδιοτιμών και χαρακτηριστικού πολυωνύμου
    disp('Ιδιοτιμές του A2:'); % Εμφανίζει μήνυμα στην οθόνη
    disp(eigenvals_A2); % Εμφανίζει τις ιδιοτιμές του πίνακα A2

    % Υπολογισμός ιδιοδιανυσμάτων για τον A2
    [V, D] = eig(A2); % Υπολογίζει τα ιδιοδιανύσματα και τις ιδιοτιμές του πίνακα A2
    disp('Ιδιοδιανύσματα του A2:'); % Εμφανίζει μήνυμα στην οθόνη
    disp(V); % Εμφανίζει τα ιδιοδιανύσματα του πίνακα A2

    % Έλεγχος αν ο A2 είναι διαγωνοποιήσιμος
    if rank(V) == 3 % Υπολογίζει το βαθμό του πίνακα V και ελέγχει αν είναι 3
        disp('Ο A2 είναι διαγωνοποιήσιμος επειδή έχει πλήρες σύνολο γραμμικά ανεξάρτητων ιδιοδιανυσμάτων'); % Εμφανίζει μήνυμα στην οθόνη
    else
        disp('Ο A2 δεν είναι διαγωνοποιήσιμος'); % Εμφανίζει μήνυμα στην οθόνη
    end

    if det(A2) ~= 0 % Υπολογίζει την ορίζουσα του πίνακα A2 και ελέγχει αν είναι διαφορετική από το μηδέν
        A2_inv = inv(A2); % Υπολογίζει τον αντίστροφο του πίνακα A2
        disp('\n\n'); % Εμφανίζει δύο κενές γραμμές στην οθόνη
        disp('Αντίστροφος του A2:'); % Εμφανίζει μήνυμα στην οθόνη
        disp(A2_inv); % Εμφανίζει τον αντίστροφο του πίνακα A2
    else
        disp('Ο πίνακας A2 είναι ιδιομορφικός και δεν μπορεί να αντιστραφεί.'); % Εμφανίζει μήνυμα στην οθόνη
    end

    % Ανάλυση για τον πίνακα A από την Άσκηση 1
    A = [1 0 -1 0; 0 2 3 -1; -1 3 2 2; 0 -1 2 1];
    [V_A, D_A] = eig(A); % Υπολογίζει τα ιδιοδιανύσματα και τις ιδιοτιμές του πίνακα A
    disp('Ιδιοτιμές του A:'); % Εμφανίζει μήνυμα στην οθόνη
    disp(diag(D_A)); % Εμφανίζει τις ιδιοτιμές του πίνακα A
    disp('Ιδιοδιανύσματα του A:'); % Εμφανίζει μήνυμα στην οθόνη
    disp(V_A); % Εμφανίζει τα ιδιοδιανύσματα του πίνακα A

    % Επίλυση συστημάτων χρησιμοποιώντας ιδιοδιανύσματα
    b1 = [1; 0; -1; 1];
    b2 = [0; -1; 0; 1];

    sol_b1 = V_A * inv(D_A) * inv(V_A) * b1; % Επίλυση συστήματος για το b1 χρησιμοποιώντας ιδιοδιανύσματα
    sol_b2 = V_A * inv(D_A) * inv(V_A) * b2; % Επίλυση συστήματος για το b2 χρησιμοποιώντας ιδιοδιανύσματα

    disp('Λύση για το b1:'); % Εμφανίζει μήνυμα στην οθόνη
    disp(sol_b1); % Εμφανίζει τη λύση για το b1
    disp('Λύση για το b2:'); % Εμφανίζει μήνυμα στην οθόνη
    disp(sol_b2); % Εμφανίζει τη λύση για το b2
end

function [eigenvals, char_poly] = calculate_eigenvalues(A)
    % Έλεγχος αν ο πίνακας είναι 3x3 και συμμετρικός
    [m, n] = size(A); % Υπολογίζει τις διαστάσεις του πίνακα A
    if m ~= 3 || n ~= 3 % Ελέγχει αν ο πίνακας δεν είναι 3x3
        error('Ο πίνακας πρέπει να είναι 3x3'); % Εμφανίζει μήνυμα σφάλματος αν ο πίνακας δεν είναι 3x3
    end
    if ~isequal(A, A') % Ελέγχει αν ο πίνακας είναι συμμετρικός
        error('Ο πίνακας πρέπει να είναι συμμετρικός'); % Εμφανίζει μήνυμα σφάλματος αν ο πίνακας δεν είναι συμμετρικός
    end
    
    % Υπολογισμός συντελεστών χαρακτηριστικού πολυωνύμου
    a = -trace(A); % Υπολογίζει το ίχνος του πίνακα A και το αρνητικό του
    b = det(A(1:2,1:2)) + det(A(2:3,2:3)) + det([A(1,1) A(1,3); A(3,1) A(3,3)]); % Υπολογίζει το άθροισμα των οριζουσών των υποπινάκων του A
    c = -det(A); % Υπολογίζει την ορίζουσα του πίνακα A και το αρνητικό της
    char_poly = [1 a b c]; % Δημιουργεί το χαρακτηριστικό πολυώνυμο

    % Υπολογισμός ιδιοτιμών χρησιμοποιώντας τη συνάρτηση roots
    eigenvals = roots(char_poly); % Υπολογίζει τις ρίζες του χαρακτηριστικού πολυωνύμου, δηλαδή τις ιδιοτιμές
end
