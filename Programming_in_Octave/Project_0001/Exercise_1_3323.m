% Συνάρτηση για τη δημιουργία τριγωνικού πίνακα βάσει του αθροίσματος της 3ης σειράς
function tri_matrix = create_triangle_matrix(n)
    % Έλεγχος αν το n είναι μεγαλύτερο από 10
    if n <= 10
        error('n πρέπει να είναι μεγαλύτερο από 10');
    end
    % Δημιουργία πίνακα n x n με τυχαίους ακέραιους αριθμούς μεταξύ 0 και 100
    matrix = randi([0, 100], n, n);
    % Εμφάνιση του παραγόμενου πίνακα
    disp('Παραγόμενος πίνακας:');
    disp(matrix);
    % Υπολογισμός του αθροίσματος της 3ης σειράς
    row_sum = sum(matrix(3, :));
    % Εμφάνιση του αθροίσματος της 3ης σειράς
    disp(['Άθροισμα της 3ης σειράς: ', num2str(row_sum)]);
    % Έλεγχος αν το άθροισμα είναι μεγαλύτερο από 50
    if row_sum > 50
        % Μετατροπή σε άνω τριγωνικό πίνακα
        tri_matrix = triu(matrix);
        disp('Μετατροπή σε άνω τριγωνικό πίνακα επειδή το άθροισμα > 50:');
    else
        % Μετατροπή σε κάτω τριγωνικό πίνακα
        tri_matrix = tril(matrix);
        disp('Μετατροπή σε κάτω τριγωνικό πίνακα επειδή το άθροισμα <= 50:');
    end
    % Εμφάνιση του τριγωνικού πίνακα
    disp(tri_matrix);
end

% Συνάρτηση για τον πολλαπλασιασμό των περιττών σειρών με τον στρογγυλοποιημένο μέσο όρο των ζυγών στηλών
function matrix = average_even_columns(matrix)
    % Επιλογή όλων των ζυγών στηλών
    even_cols = matrix(:, 2:2:end);
    % Υπολογισμός του μέσου όρου όλων των στοιχείων στις ζυγές στήλες
    mean_even = mean(even_cols(:));
    % Στρογγυλοποίηση του μέσου όρου στον πλησιέστερο ακέραιο
    mean_even_rounded = round(mean_even);
    % Εμφάνιση του στρογγυλοποιημένου μέσου όρου
    disp(['Στρογγυλοποιημένος μέσος όρος των ζυγών στηλών: ', num2str(mean_even_rounded)]);
    % Αν ο μέσος όρος είναι 0, τον ορίζουμε σε 1 για να αποφύγουμε τον πολλαπλασιασμό με το μηδέν
    if mean_even_rounded == 0
        mean_even_rounded = 1;
    end
    % Πολλαπλασιασμός όλων των στοιχείων των περιττών σειρών με τον στρογγυλοποιημένο μέσο όρο
    matrix(1:2:end, :) = matrix(1:2:end, :) * mean_even_rounded;
    % Εμφάνιση του τροποποιημένου πίνακα
    disp('Τροποποιημένος πίνακας μετά τον πολλαπλασιασμό των περιττών σειρών με τον στρογγυλοποιημένο μέσο όρο:');
    disp(matrix);
end


% Ορισμός της διάστασης n του τετραγωνικού πίνακα
n = 12; % οποιοσδήποτε ακέραιος μεγαλύτερος από 10

try
    % Κλήση της συνάρτησης για τη δημιουργία τριγωνικού πίνακα
    tri_matrix = create_triangle_matrix(n);
    % Κλήση της συνάρτησης για την τροποποίηση του πίνακα με τον μέσο όρο των ζυγών στηλών
    result_matrix = average_even_columns(tri_matrix);
    % Εμφάνιση του τελικού αποτελέσματος πίνακα
    disp('Τελικός πίνακας αποτελέσματος:');
    disp(result_matrix);
catch ME
    disp('An error occurred:');
    disp(ME.message);
end





