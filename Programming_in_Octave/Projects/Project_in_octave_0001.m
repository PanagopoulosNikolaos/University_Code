% Συνάρτηση για τον έλεγχο του πλήθους των περιττών και τον υπολογισμό του αντιστρόφου ή ψευδοαντιστρόφου
function inv_matrix = check_odd_count_and_invert(matrix)
    % Καταμέτρηση του αριθμού των περιττών στοιχείων στον πίνακα
    odd_count = sum(mod(matrix(:), 2) ~= 0);
    % Εμφάνιση του αριθμού των περιττών στοιχείων
    disp(['Αριθμός περιττών στοιχείων στον πίνακα: ', num2str(odd_count)]);
    % Έλεγχος αν ο αριθμός είναι ζυγός
    if mod(odd_count, 2) == 0 % Ζυγός αριθμός περιττών στοιχείων
        disp('Ζυγός αριθμός περιττών στοιχείων. Υπολογισμός αντιστρόφου.');
        % Έλεγχος αν ο ορίζοντας είναι κοντά στο μηδέν (ο πίνακας είναι ιδιάζων)
        if abs(det(matrix)) < 1e-12
            error('Ο πίνακας είναι κοντά σε ιδιάζων ή κακώς κλιμακωμένος. Δεν μπορεί να υπολογιστεί το αντίστροφο.');
        end
        % Υπολογισμός του αντιστρόφου του πίνακα
        inv_matrix = inv(matrix);
    else % Περιττός αριθμός περιττών στοιχείων
        disp('Περιττός αριθμός περιττών στοιχείων. Υπολογισμός ψευδοαντιστρόφου.');
        % Υπολογισμός του ψευδοαντιστρόφου του πίνακα
        inv_matrix = pinv(matrix);
    end
    % Εμφάνιση του προκύπτοντος πίνακα
    disp('Προκύπτων πίνακας:');
    disp(inv_matrix);
end

