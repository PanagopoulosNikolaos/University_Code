function main()
    % Άσκηση 3: Ανάλυση Ιδιοτιμών και Ιδιοδιανυσμάτων
    % Συγγραφέας: Το Όνομά Σας
    % Ημερομηνία: Τρέχουσα Ημερομηνία
    
    % Δοκιμή της συνάρτησης με τον πίνακα B
    B = [1 5 10; 5 1 3; 10 3 1];
    disp('Ανάλυση για τον πίνακα B:');
    disp('Πίνακας B =');
    disp(B);
    [eigenvals_B, char_poly_B] = calculate_eigenvalues(B);
    disp('Συντελεστές χαρακτηριστικού πολυωνύμου:');
    disp(char_poly_B);
    disp('Ιδιοτιμές του B:');
    disp(eigenvals_B);

    % Ανάλυση για τον πίνακα A2
    A2 = [-1 5 2; 5 -1 2; 2 2 2];
    disp('Ανάλυση για τον πίνακα A2:');
    disp('Πίνακας A2 =');
    disp(A2);
    [eigenvals_A2, char_poly_A2] = calculate_eigenvalues(A2);
    disp('Ιδιοτιμές του A2:');
    disp(eigenvals_A2);

    % Υπολογισμός ιδιοδιανυσμάτων για τον A2
    [V, D] = eig(A2);
    disp('Ιδιοδιανύσματα του A2:');
    disp(V);

    % Έλεγχος αν ο A2 είναι διαγωνοποιήσιμος
    if rank(V) == 3
        disp('Ο A2 είναι διαγωνοποιήσιμος επειδή έχει πλήρες σύνολο γραμμικά ανεξάρτητων ιδιοδιανυσμάτων');
    else
        disp('Ο A2 δεν είναι διαγωνοποιήσιμος');
    end

    % Υπολογισμός αντιστρόφου του A2
    if det(A2) ~= 0
        A2_inv = inv(A2);
        disp('\n\n');
        disp('Αντίστροφος του A2:');
        disp(A2_inv);
    else
        disp('Ο πίνακας A2 είναι ιδιομορφικός και δεν μπορεί να αντιστραφεί.');
    end

    % Ανάλυση για τον πίνακα A από την Άσκηση 1
    A = [1 0 -1 0; 0 2 3 -1; -1 3 2 2; 0 -1 2 1];
    [V_A, D_A] = eig(A);
    disp('Ιδιοτιμές του A:');
    disp(diag(D_A));
    disp('Ιδιοδιανύσματα του A:');
    disp(V_A);

    % Επίλυση συστημάτων χρησιμοποιώντας ιδιοδιανύσματα
    b1 = [1; 0; -1; 1];
    b2 = [0; -1; 0; 1];

    sol_b1 = V_A * inv(D_A) * inv(V_A) * b1;
    sol_b2 = V_A * inv(D_A) * inv(V_A) * b2;

    disp('Λύση για το b1:');
    disp(sol_b1);
    disp('Λύση για το b2:');
    disp(sol_b2);
end

function [eigenvals, char_poly] = calculate_eigenvalues(A)
    % Έλεγχος αν ο πίνακας είναι 3x3 και συμμετρικός
    [m, n] = size(A);
    if m ~= 3 || n ~= 3
        error('Ο πίνακας πρέπει να είναι 3x3');
    end
    if ~isequal(A, A')
        error('Ο πίνακας πρέπει να είναι συμμετρικός');
    end
    
    % Υπολογισμός συντελεστών χαρακτηριστικού πολυωνύμου
    a = -trace(A);
    b = det(A(1:2,1:2)) + det(A(2:3,2:3)) + det([A(1,1) A(1,3); A(3,1) A(3,3)]);
    c = -det(A);
    char_poly = [1 a b c];
    
    % Υπολογισμός ιδιοτιμών χρησιμοποιώντας τη συνάρτηση roots
    eigenvals = roots(char_poly);
end
